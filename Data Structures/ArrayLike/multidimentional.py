# MultiDimensional array in Python
# A multi-dimensional array is an array of arrays. It is a data structure that allows you to store data in a tabular form, where each element can be accessed using multiple indices. In Python, multi-dimensional arrays can be created using nested lists or by using libraries like NumPy.

arr = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Accessing elements in a multi-dimensional array
print(arr[0][0])  # Output: 1
print(arr[1][2])  # Output: 6

# print full array
print(arr)  # Output: [[1, 2, 3], [4, 5, 6], [7, 8, 9]]