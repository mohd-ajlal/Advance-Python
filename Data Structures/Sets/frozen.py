# Frozen Sets in Python

# A frozen set is an immutable version of a set. Once created, the elements of a frozen set cannot be changed, added, or removed. Frozen sets are hashable and can be used as keys in dictionaries or elements in other sets. They are defined using the frozenset() constructor.

# Creating a frozen set
frozen_set = frozenset([1, 2, 3, 4, 5])
print(frozen_set)  # Output: frozenset({1, 2, 3, 4, 5})

