# Dictionary in Python
# A dictionary is a collection of key-value pairs. Each key is unique and maps to a specific value. Dictionaries are mutable, meaning you can change their content after creation. They are defined using curly braces {} and key-value pairs are separated by colons (:).

# Creating a dictionary
my_dict = {
    "name": "Mohd Ajlal",
    "age": 22,
    "city": "Lucknow"
}

# Accessing values in a dictionary
print(my_dict["name"])  # Output: Mohd Ajlal

# print full dictionary
print(my_dict)  # Output: {'name': 'Mohd Ajlal', 'age': 22, 'city': 'Lucknow'}